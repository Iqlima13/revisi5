
<h1>VISI</h1>
<br>
<p>"Kabupaten Tasikmalaya tangguh terhadap bencana berbasis masyarakat pedesaan" </p>
<br>
<br>
<h1>MISI</h1>
<br>
<p>1. Mewujudkan masyarakat yang memiliki jiwa resiliensi terhadap bencana berbasis pada nilai religius dan agamis
<br>
2. Mewujudkan penguatan tata pengelolaan risiko bencana melalui tata kelola kepemerintahan yang baik
<br>
3. Mewujudkan pembangunan sumber daya manusia dan infrastruktur untuk terwujudnya pengurangan risiko bencana (PRB)
<br>
4. Meningkatkan manajemen pengelola risiko bencana yang berwawasan lingkungan yang berkelanjutan </p>